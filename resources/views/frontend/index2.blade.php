    function add_to_cart(dishIndex, dishID, dishName, isPizza = false, event = {}, isSetMeal = false) {

        if (selectedOrderPolicy == '') {
            $('#order_policy_selection_modal').modal('show');
        } else {
            var dishAvailability = false;
            if (cart.length > 0) {
                for (var i = 0; i < cart.length; i++) {
                    if (cart[i].dishID == dishID) {
                        dishAvailability = true;
                        break;
                    }
                }
            }

            for (var i = 0; i < cart.length; i++) {
                if(isSetMeal && cart[i].hasOwnProperty('setMeal')){
                    //console.log('equal', JSON.stringify(cart[i].setMeal.items) === JSON.stringify(setMealDealCartObj.items));
                    if (cart[i].dishID == dishID) {
                        if(JSON.stringify(cart[i].setMeal.items) === JSON.stringify(setMealDealCartObj.items)){
                            dishAvailability = true;
                            break;
                        }else{
                            dishAvailability = false;
                            break;
                        }
                    }
                } else if (isPizza) {
                    if(cart[i].hasOwnProperty('pizza')){
                        if (cart[i].dishID == dishID) {
                            if(JSON.stringify(cart[i].pizza.items) === JSON.stringify(pizzaCartObj.items)){
                                dishAvailability = true;
                                break;
                            }else{
                                dishAvailability = false;
                                break;
                            }
                        }
                    }
                }
            }

            if (dishAvailability == false) {
                if (isPizza) {
                    // dishArray[dishIndex].pizza = pizzaCartObjValidate;
                    // dishArray[dishIndex].dish_price = pizzaItemTotal;
					//console.log(dishIndex);
                    event.preventDefault();
                    var singleFoodArray = {
                        "dishID": dishID,
                        "dish_name": dishName,
                        "quantity": 1,
                        "dish_price": pizzaItemTotal,
                        "excludeFromOffer": '',
                        "pizza": pizzaCartObj
                    };
                } else if(isSetMeal){
                    event.preventDefault();
                    var singleFoodArray = {
                        "dishID": dishID,
                        "dish_name": dishName,
                        "quantity": 1,
                        "dish_price": setMealDealtotalCartPrice,
                        "excludeFromOffer": dishArray[dishIndex].exclude_from_offer,
                        "setMeal": setMealDealCartObj
                        };

                } else {
                    var singleFoodArray = {
                        "dishID": dishID,
                        "dish_name": dishName,
                        "quantity": 1,
                        "dish_price": dishArray[dishIndex].dish_price,
                        "excludeFromOffer": dishArray[dishIndex].exclude_from_offer
                    };

                }

                cart.push(singleFoodArray);
            } else {
                //cart[i].quantity = Number(cart[i].quantity) + 1;
				if (cart.length > 0) {
					for (var i = 0; i < cart.length; i++) {
						if (cart[i].dishID == dishID) {
							cart[i].quantity = Number(cart[i].quantity) + 1;
						}
					}
				}
            }



            createCart();
            $('.notification-area').css("display", "none");
            $('.notification-area').html("<p><i class='icon-bell'></i>Successfully Added to Cart</p>");
            $('.notification-area').fadeIn();
            setTimeout("clearNotification()", 1000);
        }


    }

